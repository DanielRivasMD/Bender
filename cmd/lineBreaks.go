import (
	"fmt"

	"github.com/labstack/gommon/color"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func lineBreaks() {
	for ix := 0; ix < 80; ix++ {
		color.Print(color.Grey("=", color.B))
	}

	n := 1
	for n <= 2 {
		fmt.Println()
		n++
	}
}

